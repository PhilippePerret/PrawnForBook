    Dans _PFB_, on trouve les types de paragraphe suivant^^ :
    ^^ À titre de rappel, un *paragraphe* est un texte qui se conclut par un retour de chariot. Un paragraphe est constitué de lignes, en nombre indéfini. Une *ligne* est un texte qui part de la margue gauche, avec une indentation ou non, et qui s’étire jusqu’à la marge droite, si le texte est assez long.
    * le **paragraphe de texte**. C’est un paragraphe normal et vous trouverez tout un tas d’indication sur son utilisation au fil de ce manuel,
    * le **titre**. Cf. [[titres/titres]],
    * l’**image**. Cf. [[images/grand_titre]],
    * la **table**. Cf. [[tables/titre_section]],
    * le **pfb-code** (ou *code-pfb*). Cf. [[generalites/doubles_parentheses]].
    EOT

