  description <<~EOT
    Dans cette partie, nous mentionnons rapidement les autres comportements par défaut. Certains sont détaillés dans leur partie respectives.
    * **Justification des paragraphes**. Par défaut, _PFB_ justifie les paragraphes afin que le livre ait un aspect impeccable.
    * **Pagination *intelligente***. Par défaut, _PFB_ ne numérote que les pages qu’il faut numéroter.
    * **Corrections typographiques**. _PFB_ procède à de nombreuses corrections typographiques qui permettent de produire un texte à l’aspect impeccable (espaces insécables, guillemets cohérents, exposants)
    EOT
